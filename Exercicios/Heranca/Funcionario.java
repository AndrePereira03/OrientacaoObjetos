
public class Funcionario
{
    private float horas;
    private float salario;
    
    public void setHoras(float horas)
    {
        this.horas = horas;
    }
    public float getHoras()
    {
        return this.horas;
    }
    public void setSalario(float salario)
    {
        this.salario = salario;
    }
    public float getSalario()
    {
        return this.salario;
    }
}
